class BusModel {
  String numberPlate;
  String busType;
  String busCapacity;
  String busRoute;
  String busRouteNumber;
  String busRouteStart;
  String busRouteEnd;
  String busRouteTime;
  String busRoutePrice;

  BusModel({
    required this.numberPlate,
    required this.busType,
    required this.busCapacity,
    required this.busRoute,
    required this.busRouteNumber,
    required this.busRouteStart,
    required this.busRouteEnd,
    required this.busRouteTime,
    required this.busRoutePrice,
  });
}
